package Entidades;

public class CuaRecTri {
    private int base;
    private int altura;

    public CuaRecTri(int base, int altura) {
        this.base = base;
        this.altura = altura;
    }

    public CuaRecTri() {
    }

    public int getBase() {
        return base;
    }

    public void setBase(int base) {
        this.base = base;
    }

    public int getAltura() {
        return altura;
    }

    public void setAltura(int altura) {
        this.altura = altura;
    }
}
